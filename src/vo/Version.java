package vo;

public class Version{
	private String id;
	private String ver;
	private String xgnr;
	private String bz;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getVer() {
		return ver;
	}
	public void setVer(String ver) {
		this.ver = ver;
	}
	public String getXgnr() {
		return xgnr;
	}
	public void setXgnr(String xgnr) {
		this.xgnr = xgnr;
	}
	public String getBz() {
		return bz;
	}
	public void setBz(String bz) {
		this.bz = bz;
	}
}
